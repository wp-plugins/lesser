<h1>Lesser</h1>

<p>
Lesser for WordPress allows you to use <a href="http://lesscss.org">the LESS CSS language</a> in your themes and plugins. Your WordPress site architecture is improved by maintaining your custom CSS outside of any specific theme or plugin.

<p>
<a href="http://wp-results.com/plugins/lesser-pro">Lesser PRO</a> adds professional level features to Lesser such as minification and cache-busting test mode and includes 1 year of priority support and upgrades. 

<h2>Usage</h2>

Lesser has three sections: <b>common</b>, <b>public</b>, and <b>admin</b>.

<ul>
  <li><b>common</b> LESS is included in both the public pages of your site and the admin.</li>
  <li><b>public</b> LESS is included only in your public pages.</li>
  <li><b>admin</b> LESS is included only in your admin pages.</li>
</ul>

