<p>
Lesser for WordPress allows you to use <a href="http://lesscss.org">the LESS CSS language</a> in your themes and plugins. Your WordPress site architecture is improved by maintaining your custom CSS outside of any specific theme or plugin.

<p>
<a href="http://wp-results.com/plugins/lesser">Lesser PRO</a> adds professional level features to Lesser such as minification and cache-busting test mode and includes 1 year of priority support and upgrades. 

<h2>Usage</h2>

Lesser is very easy to use. It has one tab named LESS where you input your...LESS CSS :)

